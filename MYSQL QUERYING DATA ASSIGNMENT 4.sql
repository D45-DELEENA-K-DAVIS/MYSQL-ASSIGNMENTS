-- PERSONS WITH RATING GREATER THAN 4.0
SELECT * FROM PERSONS WHERE RATING>'4.0';

-- COUNTRIES WITH POPULATION GREATER THAN 10LAKHS
SELECT *FROM COUNTRY WHERE POPULATION>1000000;

-- PERSONS WHO ARE FROM USA OR HAVING A RATING GREATER THAN 4.5
SELECT * FROM PERSONS WHERE COUNTRY_NAME='US' OR RATING>'4.5';

-- ALL PERSONS WITH THE COUNTRY NAME IS NULL
SELECT * FROM PERSONS WHERE COUNTRY_NAME IS NULL;

-- ALL PERSONS FROM THE COUNTRIES US,CANADA AND UK
SELECT *FROM PERSONS WHERE COUNTRY_NAME IN('UK','CANADA','US');

-- ALL PERSONS NOT FROM THE COUNTRIES INDIA AND AUSTRALIA
SELECT * FROM PERSONS WHERE COUNTRY_NAME NOT IN('INDIA','AUSTRALIA');

-- ALL COUNTIES WITH A POPULATION BETWEEN 5LAKHS AND 20 LAKHS
SELECT * FROM COUNTRY WHERE POPULATION BETWEEN 500000 AND 2000000;

-- ALL COUNTRIES WHOSE NAME DO NOT START WITH C  
SELECT * FROM COUNTRY WHERE COUNTRY_NAME NOT LIKE 'C%';

 
 
 
 
 


